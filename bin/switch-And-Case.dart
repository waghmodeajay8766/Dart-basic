void main(List<String> args) {
  var name = 'Ajay';
  switch (name) {
    case 'Narayan':
      print("My Name Is Narayan");
      break;
    case 'Ajay':
      print("My Name Is Ajay");
      break;
    case 'Sachin':
      print("My Name Is Sachin");
      break;
    default:
      print("Your Name Is InValid");
  }
}
