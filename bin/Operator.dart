void main() {
  //#Operators
//1]Arithmetic Operator:-
  var a = 10;
  var b = 20;
  print(a + b);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);
  print(a ~/ b);

//2]prifix and postfix increment and decriment operators
